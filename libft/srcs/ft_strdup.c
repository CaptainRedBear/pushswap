#include "libft.h"

char	*ft_strdup(char const *s1)
{
	int		i;
	int		j;
	char	*s2;

	i = 0;
	while (s1[i] != '\0')
		i++;
	if (!(s2 = (char*)malloc(sizeof(*s1) * (i + 1))))
		return (NULL);
	j = 0;
	while (j < i)
	{
		s2[j] = s1[j];
		j++;
	}
	s2[j] = '\0';
	return (s2);
}
